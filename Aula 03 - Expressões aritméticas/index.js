

// Operações básicas
let students = 20;
students = students + 1; // Somando
console.log(students)

students = students - 3 // Subtraindo
console.log(students)

students = students * 2 // Multiplicando
console.log(students)

students = students / 3 // Dividindo
console.log(students)

// Utilizando módulos
students = 20 // Reatribuido novo valor para a var students
let extraStudents = students % 3 // Nova var recebe o resto da div da var students por 3
console.log(extraStudents)

// Expressão numérica e ordem de calculo
let result = 1 + 2 * (3 + 4);
console.log(result);

result = (1 + 2) * (3 + 4);
console.log(result);

